#include "libft.h"

void    ft_chrtochr(char *s, char c1, char c2)
{
    int i;

    i = 0;
    if (s)
    {
        while (s[i] != '\0')
        {
            if (s[i] ==  c1)
            {
                c1 = c2;
            }
            i++;
        }
    }
}
