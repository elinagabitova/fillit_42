int ft_count_char(char *s, char n)
{
	int i;
	int count;

	count = 0;
	i = 0;
	if (s)
	{
		while (s[i] != '\0')
		{
			if (s[i] == n)
				count++;
			i++;
		}
	}
	return (count);
}